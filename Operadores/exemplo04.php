<?php


$a = 55.0;
$b = 55;

// Operador maior

var_dump($a > $b);

echo"<br>";

//
var_dump($a < $b);

echo"<br>";


// ****** Operador de comparação

// comparação de igualdade (compara valores, não tipo)
var_dump($a == $b);

echo"<br>";

// comparação se os valores são identicos (compara valor e tipo)

var_dump($a === $b);

echo"<br>";


// comparação se os valores são diferentes

var_dump($a != $b);

echo"<br>";





?>